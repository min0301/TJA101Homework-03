package hw1;

public class Hw16 {
	public static void main(String[] args) {
		
		System.out.println(5+5);
		//代表兩個int 5 相加 
		//所以 5 + 5 = 10
		
		System.out.println(5+'5');
		//代表 5 + '5' 的 Unicode 相加
		//5 的 Unicode 為 16進制的 35 = 10進制的 53
		//所以 5 + 53 =58
		
		System.out.println(5+"5");
		//等於兩個字串的相連
		//所以 5 + "5" = 55
	}

}
